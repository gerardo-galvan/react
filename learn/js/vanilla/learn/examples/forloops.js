/*
Loops are another form of control flow. They "loop" through a block of code until an 
original specified condition is no longer true.

For loops are an incredibly useful kind of loop. 
Always make sure your loop eventually evaluates to false or you will create what is 
called an infinite loop and crash the browser.
*/

for(i = 0; i <= 10; i++){
    console.log(i);
}